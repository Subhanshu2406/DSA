void solve() {
    int n ; 
    cin >> n;
    vector<int> arr(n);
    for(int i = 0;i<n;i++){
        cin >> arr[i];
    }
    sort(arr.begin(),arr.end());
    int count = 0;

    for(int i = 0;i<n-2;i++){
        int l = i+1;
        int r = n-1;
        while(l < r){
            if(arr[i] + arr[l] < arr[r]) l++;
            else if(arr[i] + arr[l] + arr[r] < arr[n-1] && r != n-1) l++;
        }
    }
    cout << count;
    cout << "\n";
    return;
}

